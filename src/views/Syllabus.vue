                // Scroll with some offset to account for header
                const yOffset = -20 // 20px offset from top
                const y = gradeCard.getBoundingClientRect().top + window.pageYOffset + yOffset
                window.scrollTo({ top: y, behavior: 'smooth' })
              }
            }, 150)
          }
        }
      }
    })
